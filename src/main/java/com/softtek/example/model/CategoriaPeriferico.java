
package com.softtek.example.model;


public class CategoriaPeriferico {
    
    private int id;
    private String nombre;

    public CategoriaPeriferico(int id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }
    
    
}
